function getProfile(industry) {
  for (const [key, profile] of Object.entries(profiles)) {
    if (industry.includes(key)) return { ...profile };
  }
  return { ...defaultProfile };
}

function getMeta(industry) {
  for (const [key, meta] of Object.entries(metadata)) {
    if (industry.includes(key)) return { ...meta };
  }
  return { ...defaultMeta };
}

// Add small random variance per company (±5) to differentiate companies in same industry
function addVariance(profile, seed) {
  const result = {};
  const keys = Object.keys(profile);
  for (let i = 0; i < keys.length; i++) {
    const k = keys[i];
    // Deterministic pseudo-random based on seed + param index
    const hash = ((seed * 31 + i * 17) % 11) - 5; // -5 to +5
    result[k] = Math.max(0, Math.min(100, profile[k] + hash));
  }
  return result;
}

let count = 0;
data.forEach((group, gi) => {
  const baseProfile = getProfile(group.industry);
  const meta = getMeta(group.industry);
  group.companies.forEach((company, ci) => {
    company.requiredProfile = addVariance(baseProfile, gi * 100 + ci);
    company.emoji = meta.emoji;
    company.tags = [...meta.tags];
    company.uniCorr = { ...meta.uniCorr };
    company.bgCorr = { ...meta.bgCorr };
    count++;
  });
});

